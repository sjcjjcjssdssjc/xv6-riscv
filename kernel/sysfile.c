//offset and addr is always 0 
//return the virtual address mapped
//prot is PROT_READ or PROT_WRITE or both.
uint64 
sys_mmap(void)
{
  struct file *f;
  uint64 va,length;//length
  int prot,flags;
  //no need for 0(allocate by kernel)
  //no need for 5:offset
  if(argaddr(1, &length) < 0 || argint(2, &prot) < 0 || argint(3, &flags) < 0 || argfd(4, 0, &f) < 0)
    return -1;
  if((flags & MAP_SHARED) && ((f->readable && !(prot & PROT_READ))  
  || ((!f->readable) && (prot & PROT_READ)))){
    return -1;
  }
  if((flags & MAP_SHARED) && ((f->writable && !(prot & PROT_WRITE)) 
  || ((!f->writable) && (prot & PROT_WRITE)))){
    return -1;
  }
  va = now;
  for(;;va += PGSIZE){ 
    int ok = 1;
    for(uint64 i = 0; i < length; i += PGSIZE){
      if(mmapwalk(myproc()->pagetable,va + i) != 0){
        ok = 0;
        break;
      }
      for(int j = 0; j < tot; j++){
        if(va + i >= vma[j].base && va + i < vma[j].base + length){
          ok = 0;
          break;
        }
      }
    }
    if(ok==1)break;
  }
  vma[tot].f=f;
  vma[tot].length=length;
  vma[tot].prot=prot;
  vma[tot].flags=flags;
  vma[tot].base=va;
  vma[tot].pid=myproc()->pid;
  filedup(f);
  tot++;
  //printf("map %d %p %p %p\n",tot,vma[tot-1].base,vma[tot-1].va,vma[tot-1].length);
  now = va + length;
  return va;
}